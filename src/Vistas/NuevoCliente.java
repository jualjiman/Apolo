    private void inicializar(){
        this.tfNombre.setPlaceholder("多Cual es su nombre?");
        this.tfAp.setPlaceholder("多Cual es su apellido paterno?");
        this.tfAm.setPlaceholder("多Cual es su apellido materno?");
        this.tfTelefono.setPlaceholder("多Cual es su telefono?");
    }
    
    private boolean Validar(){
        boolean ok = true;
        if(this.tfNombre.getText().equals(this.tfNombre.getPlaceholder())){
            this.tfNombre.setErrorStatus(true);
            ok = false;
        }
        if(this.tfAp.getText().equals(this.tfAp.getPlaceholder())){
            this.tfAp.setErrorStatus(true);
            ok = false;
        }
        if(this.tfAm.getText().equals(this.tfAm.getPlaceholder())){
            this.tfAm.setErrorStatus(true);
            ok = false;
        }
        if(this.tfTelefono.getText().equals(this.tfTelefono.getPlaceholder())){
            this.tfTelefono.setErrorStatus(true);
            ok = false;
        }
        if(this.cbTipo.getSelectedIndex() == 0){
            this.cbTipo.setErrorStatus(true);
            ok = false;
        } 
        return ok;
    }
    
